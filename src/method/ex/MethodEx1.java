package method.ex;

public class MethodEx1 {
    public static void main(String[] args) {
        int a = 1, b = 2, c = 3;
        int sum = a + b + c;
        double avg = sum / 3.0;

        System.out.println("평균값 : " + avg);

        int x = 15, y = 25, z = 35;
        sum = x + y + z;
        avg = sum / 3.0;

        System.out.println("평균값 : " + avg);
    }
}
